def in2cm(inches: float) -> float:
    return inches * 2.54

def in2mm(inches: float) -> float:
    return in2cm(inches) * 10

def awg2in(american_wire_gauge: int) -> float:
    """ Returns the diameter of the given wire in inches """
    return _awg_table[american_wire_gauge]

def awg2mm(american_wire_gauge: int) -> float:
    """ Returns the diameter of the given wire in millimeters """
    return in2mm(awg2in(american_wire_gauge))